export function generateSurahAudio(reciterKey) {
  const reciter = RECITERS[reciterKey]
  if (!reciter) return []

  return Array.from({ length: 114 }, (_, i) => {
    const surahNumber = i + 1
    const padded = String(surahNumber).padStart(3, "0")
    const surahName = SURAH_DATA[i]

    return {
      id: surahNumber,
      surahNumber,
      surahName,
      audioUrl: `${reciter.server}${padded}.mp3`
    }
  })
}
